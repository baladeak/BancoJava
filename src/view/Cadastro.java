    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        try {
            Double salario;
            String aux = txtSaldo.getText();
            salario = Double.valueOf(aux);

            Cliente cliente = new Cliente(txtNome.getText(), txtCPF.getText());

            Conta conta = new Conta((banco.size()), salario);
            Banco bancoTemp = new Banco(cliente, conta);
            this.banco.add(bancoTemp);

            JOptionPane.showMessageDialog(null, "Conta criada com sucesso!");
            txtNome.setText("");
            txtCPF.setText("");
            txtSaldo.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Dados inv√°lidos");
        }


    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        System.out.println("**-------------- Contas -------------- **");
        for (int i = 0; i < banco.size(); i++) {

            System.out.println("Nome: " + banco.get(i).clientes.getNome());
            System.out.println("CPF: " + banco.get(i).clientes.getCpf());
            System.out.println("Conta: " + banco.get(i).contas.getNumero());
            System.out.println("Saldo: " + banco.get(i).contas.getSaldo());
            System.out.println("");
        }

    }//GEN-LAST:event_btnListarActionPerformed

